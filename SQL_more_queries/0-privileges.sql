-- Cette commande affiche les privilèges accordés à l'utilisateur "user_0d_1" lorsqu'il se connecte depuis "localhost".
-- Elle est utile pour vérifier les permissions spécifiques de cet utilisateur sur la base de données.
SHOW GRANTS FOR 'user_0d_1'@'localhost';
