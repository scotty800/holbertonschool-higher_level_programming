-- Cette commande sélectionne les colonnes "score" et "name" de la table "second_table" 
-- et affiche les résultats triés par "score" de manière décroissante (du plus élevé au plus bas).
-- Elle est utile pour obtenir une liste des scores en ordre décroissant, en associant chaque score à son nom correspondant.
SELECT score, name FROM second_table ORDER BY score DESC;